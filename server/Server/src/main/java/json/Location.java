public class Location {

    private String country;
    private String city;
    private String latitude;
    private String longitude;

    //Getters
    /**
     * @return country
     */
    public String getCountry() {
        return country;
    }
    /**
     * @return city
     */
    public String getCity() {
        return city;
    }
    /**
     * @return latitude
     */
    public String getLatitude() {
        return latitude;
    }
    /**
     * @return longitude
     */
    public String getLongitude() {
        return longitude;
    }
}
